from tkinter import *

root = Tk()
root.title("나도 GUI")
root.geometry("640x480")

# # 기존 .pack()을 이용한 구현은 버튼/위젯을 쌓는 느낌이라면, grid는 위치를 배정하는 것
# row: y축 좌표 / column: x축 좌표
########################################################################################
# 애플 키보드 제작

# 맨 윗줄
btn_f16 = Button(root, text="F16", width=5, height=2)
btn_f17 = Button(root, text="F17", width=5, height=2)
btn_f18 = Button(root, text="F18", width=5, height=2)
btn_f19 = Button(root, text="F19", width=5, height=2)

btn_f16.grid(row=0, column=0, sticky=N+E+W+S, padx=3, pady=3) # sticky=(방향)을 사용하면, 해당 버튼이 입력한 방향으로 달라 붙는다.
btn_f17.grid(row=0, column=1, sticky=N+E+W+S, padx=3, pady=3)
btn_f18.grid(row=0, column=2, sticky=N+E+W+S, padx=3, pady=3)
btn_f19.grid(row=0, column=3, sticky=N+E+W+S, padx=3, pady=3)
# Button 초기화 시에 padx, pady를 입력하면 버튼의 크기를, grid에서 설정하면 버튼 간 간격을 조절한다.

# clear 줄
btn_clear = Button(root, text="clear", width=5, height=2)
